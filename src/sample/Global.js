let color = "#000000"
let password = "0000"
let thickness = 1

export function getColor() {
  return color
}

export function setColor(c) {
  color = c
}

export function getPassword() {
  return password
}

export function setPassword(pass) {
  password = pass
}

export function getThickness() {
  return thickness
}

export function setThickness(value) {
  thickness = value
}